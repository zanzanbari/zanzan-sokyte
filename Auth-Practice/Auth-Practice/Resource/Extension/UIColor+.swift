import UIKit

extension UIColor {
    @nonobjc class var gray100: UIColor {
        return UIColor(red: 32.0 / 255.0, green: 32.0 / 255.0, blue: 32.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var red100: UIColor {
        return UIColor(red: 244.0 / 255.0, green: 89.0 / 255.0, blue: 82.0 / 255.0, alpha: 1.0)
    }
}
